#include "libft.h"

t_bool	ft_isalnum_var(int c)
{
	if (((c >= 'A') && (c <= 'Z')) || ((c >= 'a') && (c <= 'z'))
		|| ((c >= 48) && (c <= 57)) || (c == 95))
	{
		return (true);
	}
	return (false);
}
